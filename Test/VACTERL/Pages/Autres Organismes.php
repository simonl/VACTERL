		<p>Différents organismes à travers le monde s'occupent de faire connaître et de veiller sur les victimes du syndrome dans leur région. Ces 
		groupes se situent surtout aux États-Unis et en Europe, les organismes canadiens étant plutôt rares. Voici donc, si vous voulez vous renseigner 
		d'avantage sur le syndrome, quelques liens utiles.</p>
		
		<h2>Associations Francophones :</h2>
		<h3><a href="http://www.afao.asso.fr/accueil.htm">Association Francaise de l'Atrésie de L'Oesophage</a></h3>
			<p>D'après ce que j'en sais, il s'agit de l'organisme officielle de l'atrésie de l'oesophage en France. 
			À cause des liens très forts entre le syndrome de VACTERL et l'atrésie de l'oesophage, on peut aussi 
			trouver sur ce site beaucoup d'informations/statistiques sur le syndrome. </p>
		
		<h3><a href="http://vacterl.free.fr/">Le syndrome de VACTERL</a></h3>
			<p>C'est un site amateur, endossé par aucune association, qui se veut un agrégat d'informations utiles sur le syndrome. 
			L'auteur l'a créé pour combler un manque d'information facilement assimilable sur la maladie, qui était trop souvent une 
			exclusivité des sources pleines de jargon médical. Ce site m'a beaucoup inspiré à faire ce site-même et j'en remercie l'auteur.</p>
			
